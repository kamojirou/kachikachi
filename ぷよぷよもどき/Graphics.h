#pragma once
#include <windows.h>
#include <d3d9.h>		// DirectXコアライブラリ
#include <d3dx9.h>		// DirectX拡張ライブラリ

// 関数プロトタイプ宣言

///////////////////////////////////////////////////////////////////////////////////////////////
//
// DirectXGraphicsを初期化する関数
//
///////////////////////////////////////////////////////////////////////////////////////////////
void InitializeDirectXGraphics(HWND hWnd, unsigned int resolutionWidth, unsigned int resolutionHeight);


///////////////////////////////////////////////////////////////////////////////////////////////
//
// Direct3Dデバイスを取得する関数
//
///////////////////////////////////////////////////////////////////////////////////////////////
IDirect3DDevice9* GetD3DDevice();

///////////////////////////////////////////////////////////////////////////////////////////////
//
// 画像をテクスチャとして読み込む(ロードする)関数
//
///////////////////////////////////////////////////////////////////////////////////////////////
IDirect3DTexture9* LoadTexture(const char * path);
IDirect3DTexture9* LoadTextureEx(const char * path, unsigned int* width, unsigned int* height);


///////////////////////////////////////////////////////////////////////////////////////////////
//
//  DrawSprite()関数
//    四角形を表示する位置や幅・高さを引数で貰う
//    四角形に貼り付けたいテクスチャを引数で貰う
//    画像の切り取る位置や幅・高さを引数で貰う
//
///////////////////////////////////////////////////////////////////////////////////////////////
#define DS_FLIP_X  (0x00000001)	// 左右反転
#define DS_FLIP_Y  (0x00000002)	// 上下反転

void DrawSprite(float dx, float dy, float dw, float dh, IDirect3DTexture9* texture, float sx, float sy, float sw, float sh, D3DCOLOR color = D3DCOLOR_ARGB(255, 255, 255, 255), int flags = 0);

//回転軸の座標ox,oyはピクセル単位ではなく割合で渡す
void DrawSpriteEx(float dx, float dy, float dw, float dh, float ox,float oy, float radians,IDirect3DTexture9* texture, float sx, float sy, float sw, float sh, D3DCOLOR color = D3DCOLOR_ARGB(255, 255, 255, 255), int flags = 0);

///////////////////////////////////////////////////////////////////////////////////////////////
//
//  FillRect()関数
//    四角形を単色で塗りつぶす関数
//
///////////////////////////////////////////////////////////////////////////////////////////////
void FillRect(float dx, float dy, float dw, float dh, D3DCOLOR color);


///////////////////////////////////////////////////////////////////////////////////////////////
//
// 画面に文字列を描画する関数
//
///////////////////////////////////////////////////////////////////////////////////////////////
void DrawTextFormat(int x, int y, D3DCOLOR color, const char * text);


